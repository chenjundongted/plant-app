import React, { useState, useEffect } from 'react';
import { FaBars, FaTimes } from 'react-icons/fa';
import { IconContext } from 'react-icons/lib';

import {
  Nav,
  NavbarContainer,
  NavLogo,
  NavIcon,
  MobileIcon,
  NavMenu,
  NavItem,
  NavItemBtn,
  NavLinks,
  NavBtnLink,
  Button
} from './NavBarElements';

function Navbar() {
  const [click, setClick] = useState(false);
  const [button, setButton] = useState(true);

  const handleClick = () => setClick(!click);
  const closeMobileMenu = () => setClick(false);

  const showButton = () => {
    if (window.innerWidth <= 30000) {
      setButton(false);
    } else {
      setButton(true);
    }
  };

  useEffect(() => {
    showButton();
  }, []);

  window.addEventListener('resize', showButton);

  return (
    <>
      <IconContext.Provider value={{ color: '#fff' }}>
        <Nav>
          <NavbarContainer>
            <NavLogo to='/' onClick={closeMobileMenu}>
              <NavIcon />
              AppName
            </NavLogo>
            <MobileIcon onClick={handleClick}>
              {click ? <FaTimes /> : <FaBars />}
            </MobileIcon>
            <NavMenu onClick={handleClick} click={click}>
              <NavItem>
                <NavLinks to='/' onClick={closeMobileMenu}>
                  Home
                </NavLinks>
              </NavItem>
              <NavItem>
                <NavLinks to='/a' onClick={closeMobileMenu}>
                  Your Profile
                </NavLinks>
              </NavItem>
              <NavItem>
                <NavLinks to='/messages' onClick={closeMobileMenu}>
                  Messages
                </NavLinks>
              </NavItem>
              <NavItem>
                <NavLinks to='/care-reminder' onClick={closeMobileMenu}>
                  Care Reminders
                </NavLinks>
              </NavItem>
              <NavItem>
                <NavLinks to='/plant-of-the-day' onClick={closeMobileMenu}>
                  Plant of the Day
                </NavLinks>
              </NavItem>
              <NavItem>
                <NavLinks to='/a' onClick={closeMobileMenu}>
                  Account Settings
                </NavLinks>
              </NavItem>
            </NavMenu>
          </NavbarContainer>
        </Nav>
      </IconContext.Provider>
    </>
  );
}

export default Navbar;
